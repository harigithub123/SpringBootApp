package com.billing.dto;

public class SystemConstantDTO {
	
	
	private Integer systemConstantId;
	

	private String systemKey;

	private String value;

	public Integer getSystemConstantId() {
		return systemConstantId;
	}

	public void setSystemConstantId(Integer systemConstantId) {
		this.systemConstantId = systemConstantId;
	}

	public String getSystemKey() {
		return systemKey;
	}

	public void setSystemKey(String systemKey) {
		this.systemKey = systemKey;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
	
	

}
